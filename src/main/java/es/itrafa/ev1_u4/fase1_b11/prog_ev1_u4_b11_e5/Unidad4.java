package es.itrafa.ev1_u4.fase1_b11.prog_ev1_u4_b11_e5;

/**
 * Ejercicio U4_B11_E5:
 * Utilizando los operadores lógicos, imprime las tablas de verdad de a and b, a or b , a exor
 * b y a nand b. Las operaciones anteriores tienen su correspondiente operador java excepto
 * nand que la implementamos manualmente como not and. Los valores de a y b deben de estar
 * almacenados obligatoriamente para este ejercicio en arrays de tipo boolean con el siguiente
 * aspecto.
 * a
 *      false   false   false   true
 * b
 *      false   true    false   true
 *
 * y la salida será (es suficiente con el and y el or):
 *
 * L:\Programacion>java Unidad3
 * A        B       AorB    AandB   AexorB  AnandB
 * false    false   false   false   false   true
 * false    true    true    false   true    true
 * true     false   true    false   true    true
 * true     true    true    true    false   false
 * L:\Programacion>
 *
 */
public class Unidad4 {
    public static void main (String[] args){
        boolean[] a = {false, false, true, true};
        boolean[] b = {false, true, false, true};

        System.out.println("A\t\tB\t\tAorB\tAandB\tAexorB\tAnandB");
        for (int i = 0; i < a.length; i++){
            System.out.println("" + (a[i]) + "\t" + (b[i]) + "\t" +
                    (a[i] || b[i]) + "\t" + (a[i] && b[i])+ "\t" + (a[i] ^ b[i]) + "\t" + (!a[i] || !b[i]) );
        }
    }
}
